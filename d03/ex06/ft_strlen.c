int	ft_strlen(char *str)
{
	int	aux;
	int	count;

	aux = 0;
	count = 0;
	while (str[aux] != '\0')
	{
		if (str[aux] != ' ')
		{
			count++;
		}
		aux++;
	}
	return (count);
}
